package ch02.sec02;

/*
 * 교재 45 실습
 */
public class LongExample {
	public static void main(String[] args) {
		long var1 = 10;
		long var2 = 20;
		long var3 = 1000000000;
		long var4 = 10000000000L;
		
		System.out.println(var1);
		System.out.println(var2);
		System.out.println(var3);
		System.out.println(var4);
		
	}

}
